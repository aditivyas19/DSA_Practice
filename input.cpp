/*
User Input

This program takes the user's age as input and displays it on the screen.
*/

#include <iostream>
using namespace std;

int main() {

    int age;

    // Taking input from the user
    cout << "Enter your age: ";
    cin >> age;

    // Displaying the entered value
    cout << "Your age is: " << age << endl;

    return 0;
}